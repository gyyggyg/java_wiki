from langchain.prompts import PromptTemplate

# 可选章节 - 按分支树批量判断哪些block需要生成某个可选章节
BATCH_RELEVANCE_PROMPT = PromptTemplate(
    input_variables=["section_title", "section_description", "branch_tree"],
    template="""你是一个代码分析专家。以下是一个代码仓库的模块分支树，请判断哪些模块需要生成「{section_title}」章节，并选择在最合适的层级生成。

## 章节说明
{section_description}

## 模块分支树
以下展示了该分支的完整层级结构。每个模块以缩进表示层级，格式为 [模块名]，后面是该模块的功能摘要。

{branch_tree}

## 判断规则
1. 只有当模块的功能描述中明确涉及与「{section_title}」直接相关的业务逻辑时，才需要生成此章节
2. 选择最合适的层级：
   - 如果某个父模块的多个子模块都涉及同一主题，优先在父模块层级生成（避免重复）
   - 如果只有个别叶子模块涉及，则在该叶子模块生成
   - 不要同时选择父模块和它的子模块
3. 例如：状态机章节需要模块涉及状态流转（订单状态、审核流程等）；消息队列章节需要模块涉及异步消息处理（RabbitMQ、Kafka等）
4. 仅提到通用功能（如CRUD、配置管理、数据模型定义）的模块不需要

## 输出格式
请严格按以下JSON格式输出，不要包含任何其他文字：
{{"需要": ["模块名1", "模块名2"], "不需要原因": "简要说明为什么其他模块不需要"}}

如果整个分支都不需要，输出：
{{"需要": [], "不需要原因": "简要说明原因"}}"""
)
